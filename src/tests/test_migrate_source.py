def _make_rules(*pairs):
    """Build a list of MigrationRule from (pattern, replacement) pairs."""
    import re

    rules = []
    for raw_lhs, rhs in pairs:
        rules.append(
            MigrationRule(
                raw_lhs=raw_lhs,
                lhs=re.compile(re.escape(raw_lhs)),
                rhs=rhs,
                global_=True,
            )
        )
    return rules


class TestMigrateSourceDirectory:
    def test_replaces_placeholder_in_db_file(self, tmp_path):
        (tmp_path / "x.db").write_text(
            "CREATE DATABASE $OPR_M FROM $OPR_NODE\nAS PERM=0;\n",
            encoding="utf-8",
        )
        rules = _make_rules(("$OPR_M", "{{OPR_M}}"), ("$OPR_NODE", "{{OPR_NODE}}"))

        result = migrate_source_directory(str(tmp_path), rules)

        assert result.files_changed == 1
        content = (tmp_path / "x.db").read_text(encoding="utf-8")
        assert "{{OPR_M}}" in content
        assert "$OPR_M" not in content

    def test_dry_run_does_not_write(self, tmp_path):
        original = "CREATE DATABASE $OPR_M FROM $OPR_NODE;\n"
        (tmp_path / "x.db").write_text(original, encoding="utf-8")
        rules = _make_rules(("$OPR_M", "{{OPR_M}}"))

        result = migrate_source_directory(str(tmp_path), rules, dry_run=True)

        assert result.dry_run is True
        assert result.files_changed == 1
        # File must be UNCHANGED in dry-run mode.
        assert (tmp_path / "x.db").read_text(encoding="utf-8") == original

    def test_unchanged_file_not_counted(self, tmp_path):
        (tmp_path / "clean.tbl").write_text(
            "CREATE TABLE {{T_DB}}.X (Id INT);", encoding="utf-8"
        )
        rules = _make_rules(("$OPR_M", "{{OPR_M}}"))

        result = migrate_source_directory(str(tmp_path), rules)

        assert result.files_changed == 0
        assert result.files_scanned == 1

    def test_rule_hits_counted_correctly(self, tmp_path):
        (tmp_path / "a.db").write_text("$OPR_M $OPR_M $OPR_M", encoding="utf-8")
        rules = _make_rules(("$OPR_M", "{{OPR_M}}"))

        result = migrate_source_directory(str(tmp_path), rules)

        assert result.rule_hits.get("$OPR_M") == 3

    def test_recursive_walk(self, tmp_path):
        sub = tmp_path / "sub" / "deep"
        sub.mkdir(parents=True)
        (sub / "x.db").write_text("$OPR_M", encoding="utf-8")
        rules = _make_rules(("$OPR_M", "{{OPR_M}}"))

        result = migrate_source_directory(str(tmp_path), rules)

        assert result.files_changed == 1

    def test_bteq_leading_command_preserved(self, tmp_path):
        """The .IF ERRORCODE BTEQ line must survive unchanged."""
        original = (
            ".IF ERRORCODE <> 0 THEN .GOTO ERR\n\n"
            "CREATE DATABASE $OPR_M FROM $OPR_NODE\nAS PERM=0;\n"
        )
        (tmp_path / "x.db").write_text(original, encoding="utf-8")
        rules = _make_rules(("$OPR_M", "{{OPR_M}}"), ("$OPR_NODE", "{{OPR_NODE}}"))

        migrate_source_directory(str(tmp_path), rules)

        result = (tmp_path / "x.db").read_text(encoding="utf-8")
        assert ".IF ERRORCODE <> 0 THEN .GOTO ERR" in result
        assert "{{OPR_M}}" in result
        assert "{{OPR_NODE}}" in result

    def test_multiple_syntaxes_all_converted(self, tmp_path):
        """$VAR, ${VAR} and &&VAR&& in the same file all get replaced."""
        (tmp_path / "x.db").write_text("$A ${B} &&C&&", encoding="utf-8")
        rules = _make_rules(("$A", "{{A}}"), ("${B}", "{{B}}"), ("&&C&&", "{{C}}"))
        migrate_source_directory(str(tmp_path), rules)
        content = (tmp_path / "x.db").read_text(encoding="utf-8")
        assert content == "{{A}} {{B}} {{C}}"

    def test_apply_rules_to_text_does_not_touch_files(self, tmp_path):
        """Harvest can normalise legacy markers in memory."""
        source = tmp_path / "x.db"
        original = "CREATE DATABASE $OPR_M FROM $OPR_NODE;\n"
        source.write_text(original, encoding="utf-8")
        rules = _make_rules(("$OPR_M", "{{OPR_M}}"), ("$OPR_NODE", "{{OPR_NODE}}"))

        migrated, hits = apply_migration_rules_to_text(original, rules)

        assert migrated == "CREATE DATABASE {{OPR_M}} FROM {{OPR_NODE}};\n"
        assert hits == {"$OPR_M": 1, "$OPR_NODE": 1}
        assert source.read_text(encoding="utf-8") == original


