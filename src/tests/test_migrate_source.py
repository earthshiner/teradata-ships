class TestMigrateSourceCLI:
    def _invoke(self, argv):
        old = sys.argv
        sys.argv = ["td_release_packager"] + argv
        try:
            from td_release_packager.cli import main

            with pytest.raises(SystemExit) as ei:
                main()
            return int(ei.value.code) if ei.value.code is not None else 0
        finally:
            sys.argv = old

    def test_end_to_end_via_subcommand(self, tmp_path, capsys):
        src = tmp_path / "src"
        src.mkdir()
        (src / "x.db").write_text(
            "CREATE DATABASE $OPR_M FROM $OPR_NODE;\n", encoding="utf-8"
        )
        cfg = tmp_path / "tokenise.conf"
        cfg.write_text(
            "s/$OPR_M/{{OPR_M}}/g\ns/$OPR_NODE/{{OPR_NODE}}/g\n",
            encoding="utf-8",
        )

        rc = self._invoke(
            [
                "migrate-source",
                "--tokenise-config",
                str(cfg),
                "--source",
                str(src),
            ]
        )
        capsys.readouterr()

        assert rc == 0
        content = (src / "x.db").read_text(encoding="utf-8")
        assert "{{OPR_M}}" in content
        assert "$OPR_M" not in content

    def test_dry_run_via_subcommand(self, tmp_path, capsys):
        src = tmp_path / "src"
        src.mkdir()
        original = "CREATE DATABASE $OPR_M;\n"
        (src / "x.db").write_text(original, encoding="utf-8")
        cfg = tmp_path / "tokenise.conf"
        cfg.write_text("s/$OPR_M/{{OPR_M}}/g\n", encoding="utf-8")

        rc = self._invoke(
            [
                "migrate-source",
                "--tokenise-config",
                str(cfg),
                "--source",
                str(src),
                "--dry-run",
            ]
        )
        capsys.readouterr()

        assert rc == 0
        # File unchanged in dry-run mode.
        assert (src / "x.db").read_text(encoding="utf-8") == original

    def test_missing_config_returns_nonzero(self, tmp_path, capsys):
        rc = self._invoke(
            [
                "migrate-source",
                "--tokenise-config",
                str(tmp_path / "nope.conf"),
                "--source",
                str(tmp_path),
            ]
        )
        capsys.readouterr()
        assert rc == 1

    def test_missing_source_returns_nonzero(self, tmp_path, capsys):
        cfg = tmp_path / "tokenise.conf"
        cfg.write_text("s/$A/{{A}}/g\n", encoding="utf-8")
        rc = self._invoke(
            [
                "migrate-source",
                "--tokenise-config",
                str(cfg),
                "--source",
                str(tmp_path / "nope"),
            ]
        )
        capsys.readouterr()
        assert rc == 1


